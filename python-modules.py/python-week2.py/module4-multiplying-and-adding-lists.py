# Example 1: Combining two lists

# Define a list of SEC teams
sec = ['lsu', 'uga', 'bama']

# Define a list of Big 10 teams
big_10 = ['ohio state', 'michigan', 'penn state']

# Combine the two lists into one list representing playoff brackets
playoff_brackets = sec + big_10

# Print the combined list of playoff brackets
print(playoff_brackets)

